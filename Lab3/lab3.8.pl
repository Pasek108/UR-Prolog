/*******************************************************************************

3.8 Define the move/2 predicate that cyclically shifts the elements from the 
    list by 1, e.g.:

?- move([1,2,3,4], X), move(X, Y), move(Y, Z).
X = [2, 3, 4, 1],
Y = [3, 4, 1, 2],
Z = [4, 1, 2, 3]

*******************************************************************************/

move([H|T], R) :- 
  append(T, [H], R).

